template <typename T>
T sqr(const T& x) { return x*x; }

radian_t detectors::TAPS::Element::Theta() const
{
    //TODO: Reimplement without the use of the slow creation of a TVector3
    return Position().Theta();
}

radian_t detectors::TAPS::Element::Phi() const
{
    //TODO: Reimplement without the use of the slow creation of a TVector3
    return Position().Phi();
}

TVector3 detectors::TAPS::Element::Position() const
{
    return TVector3(X(),Y(), z_pos);
}

double detectors::TAPS::Element::X() const
{
    return elementdb.at(index).x;
}

double detectors::TAPS::Element::Y() const
{
    return elementdb.at(index).y;
}

double detectors::TAPS::Element::DistanceFromBeam() const
{
    return sqrt(sqr(X())+sqr(Y()));
}

detectors::TAPS::ElementType detectors::TAPS::Element::Type() const
{
    return elementdb.at(index).type;
}

detectors::TAPS::ElementShape detectors::TAPS::Element::Shape() const
{
    return elementdb.at(index).shape;
}


unsigned int TAPS::NumberOfElements()
{
    return elementdb.size();
}

TAPS::Element TAPS::GetElement(index_t index)
{
    if(index >= elementdb.size())
        throw out_of_range("TAPS Element index out of range "+ to_string(index));

    return Element(index);

}

void detectors::TAPS::SetZPos(double z)
{
   z_pos = z;
}

double detectors::TAPS::GetZPos()
{
    return z_pos;
}

